    public String fct_bfs(UnweightedGraph gr){
    BFS bf = new BFS(gr);
         List<Integer> path = new LinkedList<>();
         path=bf.getPathFrom(Canvas.graph.getSource().getId());
         Report.append("Le chemin est : "+path.toString());
         return path.toString();          
    }
    
    public String fct_dfs(UnweightedGraph gr){
        DFS df = new DFS(gr);
         List<Integer> path = new LinkedList<>();
         path=df.getPathFrom(Canvas.graph.getSource().getId());
         Report.append("Le chemin est : "+path.toString());
         return path.toString();          
    }
    
    
